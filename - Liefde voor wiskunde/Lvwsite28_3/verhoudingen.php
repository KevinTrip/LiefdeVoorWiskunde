                                                <p>Een verhouding geeft een verband aan tussen twee of meer getallen.
<p>Je kunt een verhouding gebruiken om een prijs, een aantal of een hoeveelheid uit te rekenen. 
<p> Er zijn een aantal manier waarop je een verhouding voor jezelf duidelijk kan maken.

