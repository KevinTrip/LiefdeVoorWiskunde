                                          <p>
<p><u>Voorbeeld:</u>
<p>Als appels per 2 kilo &#8364;2,96.
<p>1 kg appels kost &#8364;2.96 : 2 = &#8364;1,48.
<p>2,5 kg appels kost 2,5 x &#8364;1,48 = &#8364;3,70
<p> Dus 2,5 kg appels kost &#8364;3,70.
<p><br>
<p>Een verhouding kan aangeven hoe een deel zich tot een totaal verhoudt.
<p>Als je het totaal weer, kun je berekenen hoe groot het deel is.
<p><u>Voorbeeld:</u>
<p><span style='color:red'>4</span> op de <span style='color:blue'>5</span> flessen zijn leeg.
<p>Dit is een verhouding tussen het <span style='color:red'>aantal lege flessen (4)</span> en het <span style='color:blue'>totaal aantal flessen(5)</span>.
<p>Je kunt dit ook anders opschrijven:
<p><li><span style='color:red'>4</span> van de <span style='color:blue'>5</span> flessen zijn leeg;
<p><li><sup><span style='color:red'>4</span></sup>&frasl;<sub><span style='color:blue'>5</span></sub> van de flessen is leeg;
<p> <li>Lege flessen : totaal aantal flessen = <span style='color:red'>4</span> : <span style='color:blue'>5</span>
<p><br>
<p> <u>Voorbeeld:</u>
<p> 600 mensen stemmen over een voorstel. <sup>2</Sup>&frasl;<sub>5</sub> van de mensen stemt tegen het voorstel.
<p>Hoeveel mensen stemmen tegen het voorstel?
<p>Je rekent uit hoeveel <sup>2</sup>&frasl;<sub>5</sub> van 600 is.
<p><li> <sup>1</sup>&frasl;<sub>5</sub> van 600 = 600 : 5 = 120
<p><li><sup>2</sup>&frasl;<sub>5</sub> van 600 = 2 X 120 = 240
<p> 240 mensen stemmen tegen het voorstel.
<p><br>
<p>Als je weet hoe groot een deel is, kun je met de verhouding berekenen hoe groot het totaal is.
<p><u>Voorbeeld:</u>
<p>Bij Harry's Rijschool zijn dit jaar 36 mensen geslaagd voor hun rijexamen.
<p>Dit zijn 9 op de 10 mensen.
<p>Hoeveel mensen hebben rijexamen gedaan?
<p><li>1 op de 10 is 36 : 9 = 4 mensen
<p><li> Het totaal is: 4 x 10 = 40 mensen.
<p> Er zijn 40 mensen die rijexamen gedaan hebben.
