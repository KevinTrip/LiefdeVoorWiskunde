                                          <p>
Bij vermeningvuldigen zijn zes verschillende manieren om dit uit te voren.
<ul>
<p><li>Omkeren;
<p>Net als bij het optellen mag je ook bij het vermenigvuldigen de getallen van plek wisselen.
<p>Je mag dus het getal voor het keerteken verwisselen met het getal achter het keerteken.
<p><u>Voorbeeld:</u>
<p> 12 x 3 =
<p> Dit wordt:
<p> 3 x 12 = 36</ul>
<ul>
<p><li> Splitsen;
<p>Hierbij zal je het achterste getal splitsen om het rekenen voor jezelf makkelijker te maken.
<p> <u>Voorbeeld;</u>
<p>7 x 16 = 
<p> Als we 16 gaan spitsen in 10 en 6 
<p> Dan wordt het rekenen:
<p> 7 x 10 = 70
<p> 7 x 6 = 42
<p> 7 x 16 = 70 + 42 = 112</ul>
<ul>
<p><li>Papagraaibekken;
<p> Hierbij splits je beide getallen.
<p><u>Let op: het is wel belangrijk dat je alles met elkaar vermenigvuldigd, en je daarna alles bij elkaar optelt.</u>
<p><u>Voorbeeld:</u>
<p> 12 x 34 =
<p> Als we 12 splitsen wordt dit: 10 en 2.
<p> Als we 34 splitsen wordt dit: 30 en 4.
<p> Nu gaan we vermenigvuldigen:
<p> 10 x 30 = 300
<p> 10 x 4 = 40
<p> 2 x 30 = 60
<p> 2 x 4 = 8
<p> Na het vermenigvuldigen moeten we niet vergeten op te tellen.
<p> 300 + 40 + 60 + 8 = 408</ul>
<ul>
<p><li> Veranderen;
<p>Je gaat hierbij &#233;&#233;n van de twee getallen uit de som veranderen.
<p> Houd er wel rekening mee dat er dan wel nog iets van af moet halen of op moet tellen wat je er teveel gedaan hebt.
<p><u> Voorbeeld:</u>
<p> 6 x 49 =
<p> Dit wordt:
<p> 6 x 49(+1)=
<p> 6 x 50 - 6 x 1 =
<p> 300 - 6 = 294</ul>
<ul>
<p><li> Vergroten en verkleinen;
<p> Hierbij vermenigvuldig en deel je de getallen door hetzelfde om het voor jezelf makkelijker te maken bij het rekenen.
<p><u> Voorbeeld:</u>
<p> 240 x 25 =
<p> 240(:4) x 25(x4) =
<p> 60 x 100 = 6000</ul>
<ul>
<p> <li> Schakelen;
<p> Neem als eerste de getallen bij elkaar die je gemakkelijk kunt vermenigvuldigen en voer daarna de laatste vermenigvuldiging uit.
<p><u> Voorbeeld:</u>
<p> 4 x 9 x 25 =
<p> 9 x (4 x 25) =
<p> 9 x 100 = 900</ul><br>
<br>
<p> Zodra je vermenigvuldigd en achteraan nullen hebt staat,
<p> kun je deze weghouden.
<p> Zolang je deze aan het einde maar opnieuw terug zet.
<ul>
<p> <u>Voorbeeld</u>
<p> 250 x 800 =
<p> Nullen weghalen dan wordt het:
<p> 25 x 8 = 200
<p> Nullen terugzetten:
<p> 25<u>0</u> x 8<u>00</u> = 200<u>000</u></ul>
                                                
