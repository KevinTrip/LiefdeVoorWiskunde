      <p>
<p>Het oplossen van deelsommen kun je gemakkelijker maken op twee verschillende manieren.
<br>
<p> Denk bij deelsommen ook aan de tafels, 
<p> zo kun je in je hoofd de hele tijd zeggen welke hoeveel keer deze tafel maakt het grote getal.
<p> Ook kun je gebruik maken van de zin:
<p> "Hoevaal past mijn tweede getal in het eerste getal tot ik bij de nul uitkom."<br>
<br>
<p> Bij delen kun je gebruik maken van een staartdeling of een haakdeling.<br>
<br>
<p> <u>Let wel op, een aantal manieren van rekenen kun je niet bij delen gebruiken die je wel bij vermenigvuldigen kunt gebruiken!
<p> Bijvoorbeeld: Omkeren.</u>
<ul>
<p><li> splitsten;
<p> Bij splitsen kun je alleen het eerste getal splitsen om ervoor te zorgen dat je meerdere gemakkelijke sommen krijgt.
<p> <u> Voorbeeld:</u>
<p> 6012 : 6 =
<p> We kunnen de 6012 splitsen in: 6000 en 12.
<p> Dan krijg je:
<p> 6000 : 6 = 1000
<p> 12 : 6 = 2
<p> 6012 : 6 = 1000 + 2 = 1002</ul>
<ul>
<p><li> Vergroten of verkleinen;
<p> Je kunt de getallen veranderen door beide getallen te vermenigvuldigen of delen door hetzelfde getal.
<p><u> Voorbeeld:</u>
<p> 125 : 5 =
<p> Als we dit gaan vergroten wordt dit:
<p> 125(x 2) : 5(x 2) =
<p> 250 : 10 = 25
<p><u>Voorbeeld:</u>
<p> 35000 : 7000 =
<p> Deze kunnen we verkleinen door:
<p> 35000(: 1000) : 7000(: 1000) =
<p> 35 : 7 = 5</ul>
<br>
<p> Zodra je gaat delen en achteraan nullen hebt staat,
<p> kun je hiervan bij ieder getal even veel van deze weghouden.
<p> Hierdoor zal de uitkomst niet veranderen.
<ul>
<p> <u>Voorbeeld</u>
<p> 3600 : 90 =
<p> Nullen weghalen dan wordt het:
<p> 360 : 9 = 40</ul>
                                                
