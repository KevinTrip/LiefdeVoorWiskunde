                                                <!-- article-content -->
  <p> Optellen kun je doen op vijf verschillende manieren uitvoeren.
<ul>
<p><li> Omkeren;
<p>Bij optellen heb je de mogelijkheid om je som om te keren. 
<p>Dit houd in dat het getal voor het plus teken mag wisselen met het getal achter het plus teken.
<p><u>Voorbeeld:</u>
<p> 2,3 + 43,6 =
<p> Dit wordt:
<p>43,6 + 2,3 = 45,9</ul>
<ul>
<p><li> Rijgen;
<p> Op het moment dat het nog onduidelijk is welke getallen bij elkaar opgeteld moeten worden, is het mogelijk om het getal achter het plus teken te splitsen.
<p><u>Voorbeeld:</u>
<p>24,6 + 15,8 =
<p> Dan splitsen we de 15,8 in: 15 en 0,8
<p> Dit wordt dan:
<p> 24,6 + 15 = 39,6
<p> 39,6 + 0,8 = 40,4</ul>
<ul>
<p><li> Splitsen;
<p>Het is ook een mogelijkheid om alleen het getal achteraan, beide getallen te splitsen.
<p><u>Voorbeeld:</u>
<p>22,7 + 12,2 =
<p> Wanneer we beide getallen splitsen wordt dit:
<p> 22,7 wordt: 22 en 0,7
<p> 12,2 wordt: 12 en 0,2
<p> 22 + 12 = 34
<p> 0,7 + 0,2 = 0,9
<p> 34 + 0,9 = 34,9</ul>
<ul>
<p><li> Veranderen;
<p> Het is mogelijk om aan beide kanten van het plus teken een verandering doorvoeren. 
<p> Dan kun je een getal naar een rond getal maken.
<p> Hierbij zal je aan de ene kant dit aantal optellen en aan de andere kant aftrekken.
<p><u>Voorbeeld:</u>
<p> 19,9 + 9,9 =
<p> 19,9(+0,1) + 9,9(-0,1) =
<p> 20 + 9,8 = 29,8</ul>
<ul>
<p><li> Schakelen;
<p> Hierbij zul je een som voor je krijgen waarin je meer dan twee getallen bij elkaar moet optellen.
<p> Je zal dan eerst twee getallen bij elkaar optellen die een rond getal maken.
<p><u>Voorbeeld:</u>
<p>2,25 + 12,6 + 17,75 =
<p>12,6 + (2,25 + 17,75) =
<p> 12,6 + 20 = 32,6</ul>                                             
