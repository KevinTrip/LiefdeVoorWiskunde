                                          <p>
	<p> Ook aftrekken kun je op verschillende manieren uitvoeren om het voor jezelf makkelijker te maken. 
<p> Hierbij zijn 3 manieren die je kunt gebruiken.<br>
<br>
<p> <u>Let wel op, een aantal maniere van rekenen kun je niet bij aftrekken gebruiken die je wel bij optellen kunt gebruiken!
<p> Bijvoorbeeld: Omkeren.</u>
<ul>
<li>Rijgen;
<p>Als het je even niet meer duidelijk is wat je van je begingetal moet aftrekken, breek van het af te trekken deel op in losse stuken.
<p><u> Voorbeeld:</u>
<p>33,54 - 12,13 =
<p> Wanneer we 12,13 splitsen dan wordt dit: 12 en 0,13
<p> Het uitrekenen wordt dan:
<p> 33,54 - 12 = 21,54
<p> 21,54 - 0,13 = 20,41</ul>
<ul>
<li>Veranderen;
<p> Rekenen met een rond getal is makkelijker. 
<p> Maar om ervoor te zorgen dat je wel dezelfde uitkomst krijgt, zul je aan beide kanten hetzelfde aantal moeten optellen totdat een van de twee rond is.
<p><u>Voorbeeld:</u>
<p> 3,7 - 1,9 =
<p> Om het onszelf makkelijker te maken zullen we de 1,9 rond maken tot het 2,0 is.
<p> Dit doen we door er 0,1 bij op te tellen, en dit zullen we dus ook bij de 3.7 doen.
<p> 3,7(+0,1) - 1,9(+0,1) =
<p> 3,8 - 2,0 = 1,8</ul>
<ul>
<li> Schakelen;
<p> Maak het jezelf makkelijker door eerst twee getallen van elkaar op te tellen die er dan voor zorgt dat je rekenstappen makkelijker worden.
<p><u>Voorbeeld:</u>
<p>27,1 - 5,3 - 1,7 =
<p> Neem eerst 5,3 en 1,7 samen. Dit wordt:
<p> 27,1 - (5,3 + 1,7) =
<p> 27,1 - 7 = 20,1</ul>

                                                
